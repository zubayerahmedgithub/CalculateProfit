public class CalculateProfit {
    public static void main(String[] args) {
        //Buying Price of An Iphone
        int buyingPrice = 400;

        //Selling Price Of An Iphone
        int sellingPrice = 700;

        // Total Profit From Selling
        System.out.println("Total Profit = $" + (sellingPrice - buyingPrice));

        // Percentage Of Profit
        System.out.println("Percentage of Profit = $" + (300 * 100 / buyingPrice));


    }
}
